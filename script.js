document.addEventListener('DOMContentLoaded', function() {
  const cardSection = document.querySelector('.cardSection');
  const categoryList = document.querySelector('.categoryList');

  const categories = [...new Set(cardsData.map(card => card.category))];

  categoryList.innerHTML = '';

  categories.forEach(category => {
    const listItem = document.createElement('li');
    listItem.className = 'category';
    listItem.innerHTML = `<a href="#">${category}</a>`;
    categoryList.appendChild(listItem);
  });

  cardSection.addEventListener('click', function(event) {
    if (event.target.closest('.flip-card-click')) {
      event.target.closest('.flip-card-click').classList.toggle('flipped');
    }
  });

  categoryList.addEventListener('click', function(event) {
    event.preventDefault();
    if (event.target.tagName === 'A') {
      cardSection.innerHTML = '';

      document.querySelectorAll('.categoryList .category').forEach((category) => {
        category.classList.remove('active');
      });

      event.target.parentElement.classList.add('active');
      

      const filteredCards = cardsData.filter(card => card.category === event.target.textContent);

      shuffleArray(filteredCards);

      filteredCards.forEach(card => {
        const cardElement = `
                  <div class="flip-card-click">
                      <div class="flip-card-inner" tabindex="0">
                          <div class="flip-card-front">
                              <div class="flip-card-front-img">
                                  <img src="${card.front}" alt="" class="src" loading="lazy" draggable="false">
                              </div>
                          </div>
                          <div class="flip-card-back">
                              <div class="flip-card-back-img">
                                  <img src="${card.back}" alt="" class="src" loading="lazy" draggable="false">
                              </div>
                          </div>
                      </div>
                  </div>
              `;
        cardSection.innerHTML += cardElement;
      });
    }
  });
});

function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
}

let backToTopBtn = document.getElementById("backToTop");

window.onscroll = function() {scrollFunction()};

function scrollFunction() {
  if (document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) {
    backToTopBtn.classList.add("show");
  } else {
    backToTopBtn.classList.remove("show");
  }
}

function topFunction() {
  document.body.scrollTop = 0;
  document.documentElement.scrollTop = 0;
}
